function setup() {
    createCanvas(600, 600);
    background(255);
  }
  
  function draw() {
    noStroke();
    //Color
    fill(169, 5, 51);
    //I
    quad(225, 50, 225, 100, 375, 100, 375, 50);
    quad(225, 550, 225, 500, 375, 500, 375, 550);
    quad(260, 100, 340, 100, 340, 500, 260, 500);
    //U
    quad(175, 375, 175, 450, 425, 450, 425, 375);
    quad(125, 400, 125, 175, 200, 175, 200, 400);
    quad(475, 400, 475, 175, 400, 175, 400, 400);
    //U Serifs
    quad(95, 175, 95, 125, 230, 125, 230, 175);
    quad(505, 175, 505, 125, 370, 125, 370, 175);
    //U Bevels
    triangle(425, 450, 475, 400, 425, 400);
    triangle(175, 450, 125, 400, 175, 400);
  }